/** @file */

#ifndef CUST_TYPES_H_
#define CUST_TYPES_H_

#include <stdbool.h>
#include <stdint.h>

/**
 * @typedef u8
 * 8-bit unsigned integer
 */
typedef uint8_t u8;

/**
 * @typedef i8
 * 8-bit signed integer
 */
typedef int8_t i8;

/**
 * @typedef u16
 * 16-bit unsigned integer
 */
typedef uint16_t u16;

/**
 * @typedef i16
 * 16-bit signed integer
 */
typedef int16_t i16;

/**
 * @typedef u32
 * 32-bit unsigned integer
 */
typedef uint32_t u32;

/**
 * @typedef i32
 * 32-bit signed integer
 */
typedef int32_t i32;

/**
 * @typedef u64
 * 64-bit unsigned integer
 */
typedef uint64_t u64;

/**
 * @typedef i64
 * 64-bit signed integer
 */
typedef int64_t i64;

/**
 * @typedef f32
 * IEEE-754 single-precision floating-point number
 */
typedef float f32;

/**
 * @typedef f64
 * IEEE-754 double-precision floating-point number
 */
typedef double f64;


#endif // CUST_TYPES_H_
